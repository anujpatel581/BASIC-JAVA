package Operator;

public class AssignmentOp {  //=, +=, -=, /=, *=, %=, (to assign the value)
    public static void main(String[] args) {
        int a=10;
        int b=5;
        System.out.println(a+=b);
        System.out.println(a-=b);
        System.out.println(a/=b);
        System.out.println(a*=b);
        System.out.println(a%=b);
        
    }
    
}
